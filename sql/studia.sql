CREATE OR REPLACE VIEW prowadzacy_kursow AS
    SELECT nazwa, p.imie, p.nazwisko
    FROM kursy k
        INNER JOIN prowadzacy p
        ON k.nr_gl_prowadzacego=p.nr_prowadzacego;

CREATE OR REPLACE VIEW zapisani_studenci AS
    SELECT k.nazwa, s.nr_indeksu, s.imie, s.nazwisko
    FROM kursy_studenci q
        INNER JOIN studenci s
        ON s.nr_indeksu=q.nr_indeksu
        INNER JOIN kursy k
        ON k.nr_kursu=q.nr_kursu;

CREATE OR REPLACE VIEW nowe_linki AS
    SELECT data, k.nazwa, linkk 
    FROM linki l
        INNER JOIN kursy k
        ON k.nr_kursu=l.nr_kursu AND l.data>=NOW() AND l.data<NOW() + INTERVAL 1 DAY;

CREATE OR REPLACE VIEW biezace_zadania AS
    SELECT opis, typ, termin, k.nazwa
    FROM zadania z
        INNER JOIN kursy k
        ON k.nr_kursu=z.nr_kursu AND z.termin>=NOW();
