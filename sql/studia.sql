                     prowadzacy,
                     kursy,
                     linki,
                     kursy_studenci;

SET FOREIGN_KEY_CHECKS = 0;

CREATE TABLE studenci (
    nr_indeksu          INT         NOT NULL,
    imie                VARCHAR(16) NOT NULL,
    nazwisko            VARCHAR(16) NOT NULL,
    PRIMARY KEY (nr_indeksu)
);

CREATE TABLE prowadzacy (
    nr_prowadzacego       INT         NOT NULL,
    imie                VARCHAR(16) NOT NULL,
    nazwisko            VARCHAR(16) NOT NULL,
    PRIMARY KEY (nr_prowadzacego)
);

CREATE TABLE kursy (
    nr_kursu            INT         NOT NULL,
    nazwa               VARCHAR(127) NOT NULL,
    nr_gl_prowadzacego  INT         NOT NULL,
    FOREIGN KEY (nr_gl_prowadzacego) REFERENCES prowadzacy (nr_prowadzacego) ON DELETE CASCADE,
    PRIMARY KEY (nr_kursu)
);

CREATE TABLE linki (
    nr_linku            INT         NOT NULL AUTO_INCREMENT,
    nr_kursu            INT         NOT NULL,
    data                DATE        NOT NULL,
    linkk               VARCHAR(127) NOT NULL,
    FOREIGN KEY (nr_kursu)      REFERENCES kursy    (nr_kursu)   ON DELETE CASCADE,
    PRIMARY KEY (nr_linku)
);

CREATE TABLE zadania (
    nr_zadania          INT         NOT NULL AUTO_INCREMENT,
    nr_kursu            INT         NOT NULL,
    termin              DATE        NOT NULL,
    typ                 ENUM ('kolokwium', 'projekt', 'raport') NOT NULL,
    opis                VARCHAR(255) NOT NULL,
    FOREIGN KEY (nr_kursu)      REFERENCES kursy    (nr_kursu)   ON DELETE CASCADE,
    PRIMARY KEY (nr_zadania)
);

CREATE TABLE kursy_studenci (
    nr_indeksu          INT         NOT NULL,
    nr_kursu            INT         NOT NULL,
    FOREIGN KEY (nr_indeksu)    REFERENCES studenci (nr_indeksu) ON DELETE CASCADE,
    FOREIGN KEY (nr_kursu)      REFERENCES kursy    (nr_kursu)   ON DELETE CASCADE,
    PRIMARY KEY (nr_indeksu,nr_kursu)
);

